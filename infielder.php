				<h2 id="player_summary">2015 Summary</h2>

				<p>
					Rafael Devers is an intriguing 19 year-old third baseman prospect from the Dominican Republic. I believe he is an interesting
					prospect because what he brings to the table is something the Red Sox is currently lacking; power. Rafael projects to hit over
					30 homeruns at the young age of 19. Where Devers needs to focus his endeavours is building himself athletically so he can get
					around easier and becoming a more quality reliable fielder. At Devers' current build he is a bit more suited to become a first
					baseman than a third baseman, which is good and fine but a growing trend within the construct of the major league is their ability
					to field multiple positions. Rafael being able to offer those two roles on the squad would be beneficial for the Red Sox. Rafael
					figures to be a player similar to Mike Moustakas with a higher ceiling for hitting with power.

				</p>

