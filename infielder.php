				<p>
					While it is now difficult to consider Xander Bogaerts a prospect in the Major League nowadays due to him already
					being a success, the Aruban success-story is only 23 years of age. Xander managed to hit an incredible .320 batting
					average in his second full season with the Red Sox. He also managed to improve his fielding drastically from the often
					difficult shortstop position. Xander has shown above average size and athleticism that has people questioning what is
					in store for Xander's future? Where Xander seeks and projects to improve in career would have to be his power-hitting
					abilities while 81 RBI's last season he only managed to hit 7 homeruns a number that is shockingly low for a productive
					number 3 hitter. Another place Xander could improve would be his plate discipline, while hitting .320 in 156 games last
					season he only had 32 walks making getting on base for Xander a little more difficult. Improving those aspects of Bogaerts
					game would surely make him the best shortstop in all of Baseball.
				</p>

