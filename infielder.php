				<h2 id="player_summary">2015 Summary</h2>

				<p>
					Very few prospects warrant the attention that Yoan Moncada has received. Moncada is a five-tool prospect from
					Cuba, The Red Sox have committed 63 million dollars to feature him within their organization. While that is
					absolutely a staggering amount of money for a baseball player much less an unproven prospect, but Moncada appears
					to be the real deal with no glaring weaknesses within his overall game. Moncada can hit from both sides of the plate
					with potential to be a quality contact and power-hitter. He can field Second and Third base with the arm to fit the
					position. But maybe his best quality is his athletic ability; Moncada stands out on the field as he looks like
					a football player. His size and speed are a rarity in Major League Baseball. Moncada projects to be a switch hitting
					Robinson Cano with incredible baserunning ability. If that isn't a top prospect in the Majors than I don't know what
					is.
				</p>

