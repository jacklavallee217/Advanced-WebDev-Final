				<p>
					Sam Travis is well-known first baseman prospect playing currently for the Portland Sea Dogs. The main
					asset he provides to the game of baseball would have to be his contact hitting. Travis has a compact swing
					that lacks loft thus ensuring many of the balls he hits do not leave the ballpark and do not pop out, because
					of this swing he has found to be one of the most toughest outs in all of Minor League Baseball consistently
					hitting .300. While Travis does have a weakness for trying to catch up to inside fastballs, he has shown great
					plate discipline that is coveted in the Major League. Travis is also a very competent fielder as he has soft hands
					and is very capable of fielding balls out of the dirt. Sam Travis figures to be a productive professional baseball
					player, and projects to be much similar to Billy Butler with fielding ability.
				</p>

